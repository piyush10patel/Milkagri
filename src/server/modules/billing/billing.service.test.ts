// Property 15: Invoice closing balance equals
//   opening_balance + charges - discounts + adjustments - payments
// Validates: Requirements 9.8
// ---------------------------------------------------------------------------
describe('Property 15: Closing balance formula holds', () => {
  it('closingBalance = openingBalance + charges - discounts + adjustments - payments', () => {
    const decimalArb = fc.integer({ min: 0, max: 1000000 }).map((n) => new Prisma.Decimal(n).div(100));

    fc.assert(
      fc.property(
        decimalArb, // openingBalance
        decimalArb, // totalCharges
        decimalArb, // totalDiscounts
        decimalArb, // totalAdjustments
        decimalArb, // totalPayments
        (openingBalance, totalCharges, totalDiscounts, totalAdjustments, totalPayments) => {
          // This is the exact formula from billing.service.ts
          const closingBalance = openingBalance
            .add(totalCharges)
            .sub(totalDiscounts)
            .add(totalAdjustments)
            .sub(totalPayments);

          // Verify the formula identity
          const reconstructed = closingBalance
            .sub(openingBalance)
            .sub(totalCharges)
            .add(totalDiscounts)
            .sub(totalAdjustments)
            .add(totalPayments);

          // reconstructed should be 0 (the formula is self-consistent)
          expect(reconstructed.eq(new Prisma.Decimal(0))).toBe(true);

          // Also verify the forward formula directly
          const expected = openingBalance
            .add(totalCharges)
            .sub(totalDiscounts)
            .add(totalAdjustments)
            .sub(totalPayments);
          expect(closingBalance.eq(expected)).toBe(true);
        },
      ),
      { numRuns: 200 },
    );
  });

  it('closing balance is computed correctly in generateInvoicesForCycle', async () => {
    const decimalArb = fc.integer({ min: 0, max: 500000 }).map((n) => new Prisma.Decimal(n).div(100));

    await fc.assert(
      fc.asyncProperty(
        decimalArb, // opening balance from previous invoice
        fc.array(
          fc.record({
            quantity: fc.integer({ min: 1, max: 100 }).map((n) => new Prisma.Decimal(n).div(10)),
            price: fc.integer({ min: 1, max: 10000 }).map((n) => new Prisma.Decimal(n).div(100)),
          }),
          { minLength: 1, maxLength: 10 },
        ),
        async (openingBalance, items) => {
          // Build delivery orders from items
          const orders = items.map((item, i) => ({
            id: `order-${i}`,
            customerId: 'customer-1',
            productVariantId: 'variant-1',
            subscriptionId: `sub-${i}`,
            routeId: 'route-1',
            deliveryDate: dateFromOffset(i),
            quantity: item.quantity,
            status: 'delivered' as const,
            isAutoGenerated: true,
            createdAt: new Date(),
            updatedAt: new Date(),
            productVariant: { id: 'variant-1', productId: 'product-1' },
            skipReason: null,
            failureReason: null,
            returnedQuantity: null,
            deliveryNotes: null,
            deliveredBy: null,
            deliveredAt: null,
          }));

          mockFindMany.mockResolvedValueOnce(orders);

          // Mock customer.findUnique
          mockCustomerFindUnique.mockResolvedValueOnce({ id: 'customer-1', pricingCategory: null });

          // First findFirst: existing cycle invoice (null), second: previous invoice with opening balance
          mockFindFirst.mockResolvedValueOnce(null);
          mockFindFirst.mockResolvedValueOnce({
            id: 'prev-inv',
            closingBalance: openingBalance,
            billingCycleEnd: new Date(Date.UTC(2023, 11, 31)), // before cycle start
          });

          // Map each order to its price
          let priceIndex = 0;
          mockGetEffectivePrice.mockImplementation(async () => {
            const price = items[priceIndex % items.length].price;
            priceIndex++;
            return { price, effectiveDate: new Date(Date.UTC(2024, 0, 1)), branch: null };
          });

          let capturedInvoiceData: any = null;

          mockTransaction.mockImplementation(async (fn: Function) => {
            const tx = {
              invoice: {
                create: async (args: any) => {
                  capturedInvoiceData = args.data;
                  return { id: 'inv-1', ...args.data };
                },
              },
              invoiceLineItem: {
                createMany: async (args: any) => ({ count: args.data.length }),
              },
            };
            return fn(tx);
          });

          await generateInvoicesForCycle('2024-01-01', '2024-01-31');

          expect(capturedInvoiceData).not.toBeNull();

          // For new invoices, discounts/adjustments/payments are 0
          // So closing = opening + charges
          const expectedCharges = items.reduce(
            (sum, item) => sum.add(item.quantity.mul(item.price)),
            new Prisma.Decimal(0),
          );

          const expectedClosing = openingBalance.add(expectedCharges);

          expect(capturedInvoiceData.openingBalance.eq(openingBalance)).toBe(true);
          expect(capturedInvoiceData.totalCharges.eq(expectedCharges)).toBe(true);
          expect(capturedInvoiceData.totalDiscounts.eq(new Prisma.Decimal(0))).toBe(true);
          expect(capturedInvoiceData.totalAdjustments.eq(new Prisma.Decimal(0))).toBe(true);
          expect(capturedInvoiceData.totalPayments.eq(new Prisma.Decimal(0))).toBe(true);
          expect(capturedInvoiceData.closingBalance.eq(expectedClosing)).toBe(true);
        },
      ),
      { numRuns: 200 },
    );
  });
});


