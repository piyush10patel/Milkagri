// Property 14: Mid-month price changes apply old price before effective date
//              and new price on/after
// Validates: Requirements 9.4
// ---------------------------------------------------------------------------
describe('Property 14: Mid-month price changes apply correct price per delivery date', () => {
  beforeEach(() => vi.clearAllMocks());

  it('applies old price before change date and new price on/after change date', async () => {
    const oldPriceArb = fc.integer({ min: 1, max: 50000 }).map((n) => new Prisma.Decimal(n).div(100));
    const newPriceArb = fc.integer({ min: 1, max: 50000 }).map((n) => new Prisma.Decimal(n).div(100));
    // Price change day: between day 1 and day 28 of the cycle (offset 1-28)
    const changeDayArb = fc.integer({ min: 1, max: 28 });

    await fc.assert(
      fc.asyncProperty(
        oldPriceArb,
        newPriceArb,
        changeDayArb,
        fc.array(fc.integer({ min: 0, max: 29 }), { minLength: 1, maxLength: 20 }),
        fc.integer({ min: 1, max: 500 }).map((n) => new Prisma.Decimal(n).div(10)),
        async (oldPrice, newPrice, changeDay, deliveryDayOffsets, quantity) => {
          // Deduplicate delivery days
          const uniqueDays = [...new Set(deliveryDayOffsets)];
          if (uniqueDays.length === 0) return;

          const changeDateOffset = changeDay;
          const variantId = 'variant-price-test';

          // Build delivery orders
          const orders = uniqueDays.map((dayOffset, i) => ({
            id: `order-${i}`,
            customerId: 'customer-1',
            productVariantId: variantId,
            subscriptionId: `sub-${i}`,
            routeId: 'route-1',
            deliveryDate: dateFromOffset(dayOffset),
            quantity,
            status: 'delivered' as const,
            isAutoGenerated: true,
            createdAt: new Date(),
            updatedAt: new Date(),
            productVariant: { id: variantId, productId: 'product-1' },
            skipReason: null,
            failureReason: null,
            returnedQuantity: null,
            deliveryNotes: null,
            deliveredBy: null,
            deliveredAt: null,
          }));

          mockFindMany.mockResolvedValueOnce(orders);
          mockCustomerFindUnique.mockResolvedValueOnce({ id: 'customer-1', pricingCategory: null });
          mockFindFirst.mockResolvedValueOnce(null);
          mockFindFirst.mockResolvedValueOnce(null);

          // Mock getEffectivePrice: old price before changeDay, new price on/after
          mockGetEffectivePrice.mockImplementation(async (_productId: string, date: Date) => {
            const dayOffset = Math.round(
              (date.getTime() - new Date(Date.UTC(2024, 0, 1)).getTime()) / (86400000),
            );
            const price = dayOffset < changeDateOffset ? oldPrice : newPrice;
            return {
              price,
              effectiveDate: dayOffset < changeDateOffset
                ? new Date(Date.UTC(2024, 0, 1))
                : dateFromOffset(changeDateOffset),
              branch: null,
            };
          });

          let capturedLineItems: any[] = [];

          mockTransaction.mockImplementation(async (fn: Function) => {
            const tx = {
              invoice: {
                create: async (args: any) => ({ id: 'inv-1', ...args.data }),
              },
              invoiceLineItem: {
                createMany: async (args: any) => {
                  capturedLineItems = args.data;
                  return { count: args.data.length };
                },
              },
            };
            return fn(tx);
          });

          await generateInvoicesForCycle('2024-01-01', '2024-01-31');

          // Verify each line item used the correct price
          for (const li of capturedLineItems) {
            const dayOffset = Math.round(
              (li.deliveryDate.getTime() - new Date(Date.UTC(2024, 0, 1)).getTime()) / 86400000,
            );
            const expectedPrice = dayOffset < changeDateOffset ? oldPrice : newPrice;
            expect(li.unitPrice.eq(expectedPrice)).toBe(true);
          }
        },
      ),
      { numRuns: 200 },
    );
  });
});

